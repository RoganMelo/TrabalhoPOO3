            aux = executor;
            button1.setEnabled(false);
            button2.setEnabled(true);
            
            executor.execute(new ThreadChassi(producao,chassi,jLabel2));
            executor.execute(new ThreadCarroceria(producao, carroceria,jLabel3));
            executor.execute(new ThreadRoda(producao,roda,jLabel4));
